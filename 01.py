# This Python code snippet is asking the user to input their birth year. It then calculates the age of
# the user in the year 2024 by subtracting the birth year from 2024. Finally, it prints out the
# calculated age with a message saying "Vous aurez alors: [age] ans cette annee", which translates to
# "You will be [age] years old this year" in English.
date_naissance = int(input("Quelle votre date de naissance : "))
age = 2024 - date_naissance
print("Vous aurez alors :", age , " ans cette annee")
