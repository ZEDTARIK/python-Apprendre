# This Python code snippet is performing basic arithmetic operations on two numbers input by the user.
# Here's a breakdown of what the code does:
A = float(input("Enter the number 1 : "))
B = float(input("Enter the number 2 : "))

Somme= A + B
Produit = A * B 
Division= A / B
Deference = A - B

print("the result of A + B is : ", Somme)
print("the result A * B is : ", format(Produit, ".2f"))
print("the result A / B is : ", format(Division, ".2f"))
print("the result A - B is : ", format(Deference, ".2f"))